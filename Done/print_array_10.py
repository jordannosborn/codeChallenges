# Challenge: Print array
# Create a function called print_array that prints each element of an array


def print_array(the_array):
  the_array = ["dog", "cat", "fish"]
  for x in the_array:
    print(x)




# Assuming an array like this: 
# ["dog", "cat", "fish"]
# The function should output this:
# dog
# cat
# fish
print_array(["dog", "cat", "fish"])
